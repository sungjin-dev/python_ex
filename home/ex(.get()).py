'''
📝 예제: 신조어 번역기 만들기
요즘 유행하는 줄임말이나 신조어의 뜻을 알려주는 함수를 만들어봅시다. 이번에는 if문이나 in 연산자 없이 .get() 하나만으로 해결해 보세요!

[요구 사항]

search_slang(word) 라는 이름의 함수를 만드세요.

함수 내부에 아래의 단어와 뜻이 담긴 딕셔너리를 생성하세요.

'아아': '아이스 아메리카노'

'오운완': '오늘 운동 완료'

'중꺾마': '중요한 것은 꺾이지 않는 마음'

if 조건문 없이, 딕셔너리의 .get() 메서드를 사용하여 입력받은 word의 뜻을 찾아서 즉시 return 하세요.

만약 딕셔너리에 없는 단어를 입력받았다면 .get()의 기본값 기능을 활용하여 "아직 등록되지 않은 단어입니다."라는 문자열이 반환되도록 하세요.

함수 바깥에서 input()으로 단어를 입력받고, 함수를 호출한 뒤 변수에 담아 print()로 출력하세요. (이전과 동일)

[실행 예시]

Python
# 실행 결과 예시 1
# 뜻을 알고 싶은 신조어를 입력하세요: 오운완
# 출력 결과: 오늘 운동 완료

# 실행 결과 예시 2
# 뜻을 알고 싶은 신조어를 입력하세요: 폼미쳤다
# 출력 결과: 아직 등록되지 않은 단어입니다.'

'''

def search_slang(word):

    slangDict = {
        '아아': '아이스 아메리카노',

        '오운완': '오늘 운동 완료',

        '중꺾마': '중요한 것은 꺾이지 않는 마음'
    }

    return slangDict.get(word, '아직 등록되지 않은 단어입니다.')
    
inputSlang = input('신조어를 입력하세요.')

result = search_slang(inputSlang)

print(result)

